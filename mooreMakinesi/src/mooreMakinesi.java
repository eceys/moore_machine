    private void btn_hesaplaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hesaplaActionPerformed
        

        if (durumVeGecisleri.size() != durumlarKumesi.size()) {

            String eklenenler = "";
            for (int a = 0; a < durumVeGecisleri.size(); a++) {
                String[] eklenenlerKumesi = durumVeGecisleri.get(a).split(",");
                eklenenler = eklenenler + " " + eklenenlerKumesi[0];
            }

            JOptionPane.showMessageDialog(this, "Tüm durumlar eklenmemiş.\nLütfen tüm durumların \ngeçişlerini ve çıktılarını ekleyiniz.\n"
                    + "Eklenen durumlar : " + eklenenler, "Hata", JOptionPane.ERROR_MESSAGE);

        } else {
            

            diziInput.clear();
            String input = tf_input.getText();
            for (int a = 0; a < input.length(); a++) {
                diziInput.add(input.substring(a, a + 1));
            }
            

            int baslangicDurumu = 0;
            String sonrakiDurum;
            String[] durumGecisKume;
            
            /*Sonraki durum, bize verilen durumlar kümesinin ilk elemanı 
            olarak belirlenir. Çünkü başlangıç state ilk sırada verildiği
            düşünülmektedir.*/
            sonrakiDurum = durumlarKumesi.get(0);

            String stringSonucDurumlar = "";
            String stringSonucCiktilar = "";
            

            /*Aşağıdaki kısımda diziler içerisinde bulunan durumlara göre 
            denk gelen yerler eşleştirilerek hangi durumda nereye geçileceği bulunur.
            Örneğin a input verildiğinde devam edilen durum için semboller alfabesinde 
            a'nın sırası bulunur. Bulunan sıraya göre, bulunan durumun geçişi durumVeGecisleri 
            dizisinde bir fazlasıyla eşleşmektedir. Çünkü ilk eleman durumu ifade etmektedir. Ara
            elemanlar ise geçişleri son eleman çıktıyı vermektedir. Sonuçlar bu sıraya göre atama yapılır.*/
            for (int a = 0; a < diziInput.size(); a++) {
                for (int b = 0; b < sembollerKumesi.size(); b++) {
                    if (diziInput.get(a).equals(sembollerKumesi.get(b))) {
                        for (int c = 0; c < durumVeGecisleri.size(); c++) {
                            durumGecisKume = durumVeGecisleri.get(c).split(",");
                            if (durumGecisKume[0].equals(sonrakiDurum)) {
                                sonucKumesi.add(durumGecisKume[durumGecisKume.length - 1]);
                                sonucDurumlar.add(sonrakiDurum);
                                sonrakiDurum = durumGecisKume[b + 1];
                                break;
                            }
                        }
                    }
                }
            }

            

            /*Son eleman yukarıda eksik kalır. Çünkü bir sonraki geçiş elemanına 
            bakılamadan dizi boyutuna ulaşılmış olur. Bu yüzden yukarıdaki aşamada bir kez daha tekrar
            edilir. Son elemanda bu sayede eklenmiş olur.*/
            String enSonInput = diziInput.lastElement();
            int sonInputIndeks = 0;
            for (int a = 0; a < sembollerKumesi.size(); a++) {
                if (enSonInput.equals(sembollerKumesi.get(a)));
                sonInputIndeks = a + 1;
            }
            for (int a = 0; a < durumVeGecisleri.size(); a++) {
                durumGecisKume = durumVeGecisleri.get(a).split(",");
                if (durumGecisKume[0].equals(sonrakiDurum)) {
                    sonrakiDurum = durumGecisKume[sonInputIndeks];
                    sonucKumesi.add(durumGecisKume[durumGecisKume.length - 1]);
                    sonucDurumlar.add(sonrakiDurum);
                }
            }
            
            
            //Sonuçlar string haline getirilir.
            for (int a = 0; a < sonucDurumlar.size(); a++) {

                stringSonucDurumlar = stringSonucDurumlar + " " + sonucDurumlar.get(a);
                stringSonucCiktilar = stringSonucCiktilar + " " + sonucKumesi.get(a);
                
            }
            

            tf_sonuc_input.setText(input);
            tf_sonuc_durum.setText(stringSonucDurumlar);
            tf_sonuc_output.setText(stringSonucCiktilar);
            
            //Tablo oluşturmak için aşağıdaki adımlar uygulanır.
            ta_sonuc1.setText("");
            ta_sonuc2.setText("");
            ta_sonuc3.setText("");

            for (int a = 0; a < durumVeGecisleri.size(); a++) {
                String[] splitDurumGecis = durumVeGecisleri.get(a).split(",");
                ta_sonuc1.append(splitDurumGecis[0]);
                for (int b = 1; b < splitDurumGecis.length - 1; b++) {
                    ta_sonuc2.append(splitDurumGecis[b] + "\t");
                }
                ta_sonuc3.append(splitDurumGecis[splitDurumGecis.length - 1]);
                ta_sonuc1.append("\n");
                ta_sonuc2.append("\n");
                ta_sonuc3.append("\n");
            }

            jDialog1.show(true);
            //Sonuçlar için diyalog penceresi açılır.
            
            sonucDurumlar.clear();
            sonucKumesi.clear();
            //Gerekli diziler temizlenir.
        }


    }//GEN-LAST:event_btn_hesaplaActionPerformed

