    /*Ekleme aşamasında uyuşmayı kontrol eden fonksiyon*/
    public boolean kontrol(String eklenecekDurum) {
        
        for (int a = 0; a < durumVeGecisleri.size(); a++) {
            String kontrol[] = durumVeGecisleri.get(a).split(",");

            if (eklenecekDurum.equals(kontrol[0])) {
                return false;
            }
        }
        return true;
    }
    
    //Yazılan geçişler durum kümesinde var mı kontrol edilir.
    public int kontrolDurumlarGecisleri(String gecisler){
        int kontrol = 0;
        String[] gecislerDizi = gecisler.split(",");
        for(int a = 0 ; a < gecislerDizi.length ; a++){
            for(int b = 0 ; b < durumlarKumesi.size() ; b++){
                if(gecislerDizi[a].equals(durumlarKumesi.get(b))){
                    kontrol = 1;
                    break;
                }else{
                    kontrol = 0;
                }
            }
            if(kontrol == 0){
                JOptionPane.showMessageDialog(this, gecislerDizi[a] + " durumlar kümesinde bulunamadı!", "Hata",JOptionPane.ERROR_MESSAGE);
                return 1;
            }
        }
        return 0;
    }


    private void btn_ekle2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ekle2ActionPerformed
        
        /*Seçili olan durumun, semboller alfabesindeki sıraya göre bir sonraki
        aşamada hangi durumda olacağı ve çıktı olarak ne vereceğinin eklendiği
        bölümdür.
        */
        
        
        /*Text Field içerisindeki bilgiler dizilere eklenir.*/
        String baslangicDurumu = cb_durum.getItemAt(cb_durum.getSelectedIndex());
        String durumlarArasiGecis = tf_gecis.getText();
        String durumunCiktisi = ftf_ciktilar.getText();
        
        if(durumlarArasiGecis.equals("") || ftf_ciktilar.equals("")){
            JOptionPane.showMessageDialog(this, "Lütfen geçerli değerler giriniz.");
        }else{
            if(kontrolDurumlarGecisleri(durumlarArasiGecis) == 0){
                String[] kontrolGecis = durumlarArasiGecis.split(",");
                if (kontrolGecis.length != sembollerKumesi.size()) {
                    /*Programın sol tarafında bulunan semboller alfabesi ile yazılan geçişler 
                    aynı uzunlukta olmalıdır. DFA gibi çalıştığından her geçişde ne olacağı 
                    bilgilendirilmelidir. Aşağıdaki kod satırı ise bu iki dizinin uzunluğunu
                    karşılaştırır.
                    */
                    JOptionPane.showMessageDialog(this, "Semboller alfabesi ile geçişleri ifade eden alfabe\n"
                            + "uzunluğu birbirinden farklı olamaz.", "Hata", JOptionPane.ERROR_MESSAGE);
                } else {
                    if (durumVeGecisleri.size() <= 0) {
                        String tumIfade;
                        tumIfade = baslangicDurumu + "," + durumlarArasiGecis + "," + durumunCiktisi;
                        durumVeGecisleri.add(tumIfade);
                    } else {
                        if (kontrol(baslangicDurumu)) {
                            String tumIfade;
                            tumIfade = baslangicDurumu + "," + durumlarArasiGecis + "," + durumunCiktisi;
                            durumVeGecisleri.add(tumIfade);
                            JOptionPane.showMessageDialog(this, baslangicDurumu + " eklendi.");
                        } else {
                            JOptionPane.showMessageDialog(this, baslangicDurumu + " daha önce eklenmiş.");
                        }
                    }


                    if (durum_ekleme == 0) {
                        JOptionPane.showMessageDialog(this, "Tüm durumlar eklendiğinde hesapla butonu açılacaktır.");
                        durum_ekleme = 1;
                    }

                    if (durumVeGecisleri.size() == durumlarKumesi.size()) {
                        btn_hesapla.setEnabled(true);
                    }
                }

                tf_gecis.setText("");
                ftf_ciktilar.setText("");
            }  
        }

        


    }//GEN-LAST:event_btn_ekle2ActionPerformed

